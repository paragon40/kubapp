#!/bin/bash

# ============================================================
# KUBAPP — JSON HELPER LIBRARY
# ============================================================

# ============================================================
# JSON ESCAPING
# ============================================================

json_escape() {
    local value="${1:-}"

    jq -Rn --arg v "${value}" '$v'
}

# ============================================================
# JSON ARRAYS
# ============================================================

json_array() {
    if [[ $# -eq 0 ]]; then
        echo '[]'
        return 0
    fi

    printf '%s\n' "$@" \
        | jq -R . \
        | jq -s .
}

json_empty_array() {
    echo '[]'
}

# ============================================================
# JSON OBJECTS
# ============================================================

json_empty_object() {
    echo '{}'
}

# ============================================================
# JSON BOOLEAN
# ============================================================

json_bool() {
    local value="${1:-false}"

    if [[ "${value}" == "true" ]]; then
        echo "true"
    else
        echo "false"
    fi
}

# ============================================================
# JSON ERROR SERIALIZATION
# ============================================================

json_errors() {
    if [[ ${#ERRORS[@]} -eq 0 ]]; then
        json_empty_array
        return 0
    fi

    printf '%s\n' "${ERRORS[@]}" \
        | jq -R . \
        | jq -s .
}

# ============================================================
# JSON WARNING SERIALIZATION
# ============================================================

json_warnings() {
    if [[ ${#WARNINGS[@]} -eq 0 ]]; then
        json_empty_array
        return 0
    fi

    printf '%s\n' "${WARNINGS[@]}" \
        | jq -R . \
        | jq -s .
}
